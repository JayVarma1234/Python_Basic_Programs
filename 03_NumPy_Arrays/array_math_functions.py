import numpy as np
arr=np.array ([5,10,15,20,25])
print("Original Array:",arr)
#Sum
print("Sum of all elements:",arr.sum())
#Mean
print("Mean of Array:",arr.mean())
#Maximum
print("Maximum value of array",arr.max())
#minimum
print("Minimum value of array",arr.min())
#square if each element
sqrt_arr=np.sqrt(arr)
print("Square root:",sqrt_arr)
#standrad daviation
std_dev=np.std(arr)
print("Standard daviation:",std_dev)
#product of all element
product=np.prod(arr)
print("product:",product)
